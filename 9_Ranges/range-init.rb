#!/usr/bin/ruby

# Numeric range
otten = (1..10)

# Range conversion to array
otten_array = otten.to_a()

# Printing
puts "The range containing numbers from one to ten is: #{otten}. \nThe corresponding array is #{otten_array}"
puts "The length of the array is #{otten_array.length}"


# Alphabetical range
iqiu = ('i'.. 'q')

# Range conversion to array
iqiu_array = iqiu.to_a()

# Printing
puts "The range containing letters from 'i' to 'q' is: #{iqiu}. \nThe corresponding array is #{iqiu_array}"
puts "The length of the array is #{iqiu_array.length}"

# Using a for loop to print the range

negthtth = (-3..3)

for value in negthtth
    puts value
end

